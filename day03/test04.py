score = int(input('请输入成绩：'))
#在判断成绩是否及格前先判断成绩是否大于等于0且小于等于100
if 0 <= score <= 100:
    if score >= 60:  # 满足条件True
        # 及格了只会执行if下面语句
        print('及格了，鼓励你一下')
    else:  # 不满足条件False
        # 不及格了只会执行else下面语句
        print('尝尝你老子的七匹狼')
else:
    #如果不合法就输入输入有误
    print('输入有误')
# 判断成绩是否及格

print('程序继续执行')
