'''
需求：用户输入密码，有三次机会，如果正确登录成功
如果三次都错误，冻结账号
'''

for i in range(3):
    #提示用户输入密码
    password = input('请输入密码：')
    #默认密码为666
    if password == '666':
        print('密码正确')
        #密码正确跳出循环
        break
    else:
        print('密码不正确，请重新输入')
else:# 循环的条件不满足，或循环的序列被执行完了，进入这个else
    print('请多次输入密码错误，账户已被冻结，请联系管理员')