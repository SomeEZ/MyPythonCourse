#注释 ：行开头使用#（井号）后面的内容都是注释，注释快捷键为：Ctrl+/
'''
    多行注释：三那个单引号为多行注释，首行和末行都需要三个单引号，不能躲也不能少，否则会报错
    python会忽略注释中的所有内容，都不会执行
    注释是给程序员看的

    ！！！
    注释不能嵌套
    编码中要养成随手写注释的习惯
    注释一定要有确切的意义，例如描述每个代码块的功能或代码的使用方式

    不能写例如这个功能很牛逼，否则过段时间后就只有上帝知道这段代码是干么的了
'''
'''
多行注释前加上内容他就不再是注释了，他会变成一个多行文本内容
'''

